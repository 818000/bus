package org.miaixz.bus.cortex.builtin;

import lombok.Getter;
import lombok.Setter;

/**
 * Key-value label with an optional category for grouping.
 *
 * @author Kimi Liu
 * @since Java 21+
 */
@Getter
@Setter
public class Label {

    /**
     * Label key name.
     */
    private String key;
    /**
     * Label value.
     */
    private String value;
    /**
     * Optional category used for grouping labels.
     */
    private String category;

}
