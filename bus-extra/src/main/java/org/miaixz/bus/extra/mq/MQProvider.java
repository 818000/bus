package org.miaixz.bus.extra.mq;

/**
 * 消息队列引擎接口
 *
 * @author Kimi Liu
 * @since Java 17+
 */
public interface MQProvider {

    /**
     * 初始化配置
     *
     * @param config 配置
     * @return this
     */
    MQProvider init(MQConfig config);

    /**
     * 获取消息生产者
     *
     * @return 消息生产者
     */
    Producer getProducer();

    /**
     * 获取消息消费者
     *
     * @return 消息消费者
     */
    Consumer getConsumer();

}
