package org.miaixz.bus.goalie;

/**
 * 数据序列化接口，定义对象序列化为字符串的通用方法
 *
 * @author Kimi Liu
 * @since Java 17+
 */
public interface Provider {

    /**
     * 将对象序列化为字符串
     *
     * @param object 待序列化的对象
     * @return 序列化后的字符串结果
     */
    String serialize(Object object);

}