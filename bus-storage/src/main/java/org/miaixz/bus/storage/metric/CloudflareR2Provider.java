package org.miaixz.bus.storage.metric;

import org.miaixz.bus.storage.Context;

/**
 * Storage service provider for Cloudflare R2 S3-compatible object storage. This provider extends
 * {@link GenericS3Provider} to offer Cloudflare R2-specific configurations.
 *
 * @author Kimi Liu
 * @since Java 17+
 */
public class CloudflareR2Provider extends GenericS3Provider {

    /**
     * Constructs a Cloudflare R2 S3 provider with the given context.
     *
     * @param context The storage context, containing Cloudflare R2-specific endpoint, bucket, access key, secret key,
     *                and region.
     */
    public CloudflareR2Provider(Context context) {
        super(context);
    }

}
