package org.miaixz.bus.mapper;

/**
 * 定义排序顺序和优先级的接口，用于控制执行顺序。
 *
 * @author Kimi Liu
 * @since Java 17+
 */
public interface ORDER {

    /**
     * 升序排序
     */
    String ASC = "ASC";

    /**
     * 降序排序
     */
    String DESC = "DESC";

    /**
     * 获取执行优先级。
     *
     * @return 优先级，数值越大优先级越高，越早执行，默认为 0
     */
    default int getOrder() {
        return 0;
    }

}