package org.miaixz.bus.core.center.map.concurrent;

/**
 *
 * 确定值的选择器
 *
 * @param <V> 值类型
 * @author Kimi Liu
 * @since Java 17+
 */
public interface Weigher<V> {

    /**
     *
     * 测量对象的选择器以确定值消耗了多少个容量单位
     *
     * @param value 称量物体
     * @return 对象的选择器
     */
    int weightOf(V value);

}
