package org.miaixz.bus.core.data.masking;

/**
 * 脱敏处理器，用于自定义脱敏规则
 *
 * @author Kimi Liu
 * @since Java 17+
 */
@FunctionalInterface
public interface MaskingHandler {

    /**
     * 处理传入的数据字符串，经过脱敏逻辑后，返回处理后的值
     *
     * @param value 待处理的值
     * @return 处理后的值
     */
    String handle(CharSequence value);

}
