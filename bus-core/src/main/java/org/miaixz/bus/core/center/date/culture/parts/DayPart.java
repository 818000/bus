package org.miaixz.bus.core.center.date.culture.parts;

/**
 * Abstract base class representing day-level date-time components.
 * <p>
 * Extends month-level precision by adding the day field, enabling representation of date-time components at daily
 * granularity.
 *
 * @author Kimi Liu
 * @since Java 17+
 */
public abstract class DayPart extends MonthPart {

    /**
     * The day value (1-31)
     */
    protected int day;

    /**
     * Gets the day value.
     *
     * @return the day value (1-31)
     */
    public int getDay() {
        return day;
    }

}
