    /**
     * Internal class for storing Group statistics information.
     * <p>
     * This class encapsulates various storage and size metrics associated with a GitLab group, providing detailed
     * information about resource usage across the group's projects. Statistics include storage sizes for repositories,
     * LFS objects, and job artifacts.
     * </p>
     *
     * @author Kimi Liu
     * @since Java 21+
     */
    public class Statistics {

        /**
         * The total storage size used by the group.
         * <p>
         * This value represents the sum of all storage consumed by the group, including repositories, LFS objects, and
         * job artifacts. The value is measured in bytes.
         * </p>
         */
        private Long storageSize;

        /**
         * The total size of all repositories in the group.
         * <p>
         * This value represents the combined storage used by all Git repositories within the group's projects. The
         * value is measured in bytes.
         * </p>
         */
        private Long repositorySize;

        /**
         * The total size of all LFS (Large File Storage) objects in the group.
         * <p>
         * This value represents the storage consumed by LFS objects across all projects in the group. LFS is used for
         * storing large binary assets outside the Git repository. The value is measured in bytes.
         * </p>
         */
        private Long lfsObjectsSize;

        /**
         * The total size of all job artifacts in the group.
         * <p>
         * This value represents the storage consumed by CI/CD job artifacts across all projects in the group. Job
         * artifacts include build outputs, logs, test results, and other files generated by CI/CD pipelines. The value
         * is measured in bytes.
         * </p>
         */
        private Long jobArtifactsSize;

        /**
         * Returns the storage size.
         *
         * @return the result
         */

        public Long getStorageSize() {
            return storageSize;
        }

        /**
         * Sets the storage size.
         *
         * @param storageSize the storage size value
         */

        public void setStorageSize(Long storageSize) {
            this.storageSize = storageSize;
        }

        /**
         * Returns the repository size.
         *
         * @return the result
         */

        public Long getRepositorySize() {
            return repositorySize;
        }

        /**
         * Sets the repository size.
         *
         * @param repositorySize the repository size value
         */

        public void setRepositorySize(Long repositorySize) {
            this.repositorySize = repositorySize;
        }

        /**
         * Returns the lfs objects size.
         *
         * @return the result
         */

        public Long getLfsObjectsSize() {
            return lfsObjectsSize;
        }

        /**
         * Sets the lfs objects size.
         *
         * @param lfsObjectsSize the lfs objects size value
         */

        public void setLfsObjectsSize(Long lfsObjectsSize) {
            this.lfsObjectsSize = lfsObjectsSize;
        }

        /**
         * Returns the job artifacts size.
         *
         * @return the result
         */

        public Long getJobArtifactsSize() {
            return jobArtifactsSize;
        }

        /**
         * Sets the job artifacts size.
         *
         * @param jobArtifactsSize the job artifacts size value
         */

        public void setJobArtifactsSize(Long jobArtifactsSize) {
            this.jobArtifactsSize = jobArtifactsSize;
        }

    }

