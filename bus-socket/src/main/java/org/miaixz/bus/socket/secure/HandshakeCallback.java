package org.miaixz.bus.socket.secure;

/**
 * Callback interface for SSL/TLS handshake completion events.
 * <p>
 * Implementations of this interface can be used to perform actions once an SSL/TLS handshake has successfully
 * completed.
 * </p>
 *
 * @author Kimi Liu
 * @since Java 17+
 */
public interface HandshakeCallback {

    /**
     * Called when the SSL/TLS handshake process is complete.
     */
    void callback();

}
